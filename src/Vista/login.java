    private void bingresoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bingresoActionPerformed
        username=txtname.getText();
        contraseña=txtpass.getText();
        modeloUsuario mu=new modeloUsuario();
        usuario u=new usuario(username, contraseña);
        int acceso=mu.ingresoadmin(u);
        if(acceso==1){
            menu_admin ma=new menu_admin();
            ma.toFront();
            ma.setVisible(true);
            this.setVisible(false);
        }else{
            JOptionPane.showMessageDialog(null,"EL NOMBRE DE USUARIO Y/O CONTRASEÑA NO SON CORRECTOS");
        }
        
    }//GEN-LAST:event_bingresoActionPerformed

